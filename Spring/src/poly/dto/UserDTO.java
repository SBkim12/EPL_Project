package poly.dto;



public class UserDTO {

	private String member_pwd;
	private String member_id; 
	private String favorite_team;
	private String member_name ;
	private String member_score;
	private String member_point;
	
	
	public String getMember_pwd() {
		return member_pwd;
	}
	public void setMember_pwd(String member_pwd) {
		this.member_pwd = member_pwd;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getFavorite_team() {
		return favorite_team;
	}
	public void setFavorite_team(String favorite_team) {
		this.favorite_team = favorite_team;
	}
	public String getMember_name() {
		return member_name;
	}
	public void setMember_name(String member_name) {
		this.member_name = member_name;
	}
	public String getMember_score() {
		return member_score;
	}
	public void setMember_score(String member_score) {
		this.member_score = member_score;
	}
	public String getMember_point() {
		return member_point;
	}
	public void setMember_point(String member_point) {
		this.member_point = member_point;
	}
	
	
	
	
	
}
